public class RBTree {

    private Node root;
    private Node nil;
    private int size;
    private int height;


    public RBTree() {

    }

    public Node getRoot() {
        return root;
    }

    public Node getNil() {
        return nil;
    }

    public int getSize() {
        return size;
    }

    public int getHeight() {
        return height;
    }
}
